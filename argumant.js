function doubleIt (number){
    const doubled = number * 2;
    console.log("The double of the given number is : ", number, doubled);
}

console.log("Calling doubleIt function with 235");
doubleIt(235);
console.log("Calling doubleIt function with 1470");
doubleIt(1470);
console.log("Calling doubleIt function with 1455");
doubleIt(1455);